package ex1_lista2;

public class Rã extends Anfibio{

	public Rã() {
		this.nome = "Bufos";
	}
	
	public void Nome() {
		System.out.println("O nome da rã é: " + this.nome);
	}
	
	public void Status() {
		System.out.println("A rã está escondida");
		
	}
	
	public void Fome() {
		System.out.println("A rã está alimentada");
		
	}
	
	public void Sono() {
		System.out.println("A rã está dormindo");
		
	}
	
	public void Humor() {
		System.out.println("A rã está com o humor neutro");
		
	}
}

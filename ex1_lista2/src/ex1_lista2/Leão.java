package ex1_lista2;

public class Leão extends Terrestre {

	public Leão() {
	this.nome = "Leone";
	
	}
	public void Nome() {
		System.out.println("O nome do leão é: " + this.nome);
	}
	
	public void Status() {
		System.out.println("O leão está caçando");
		
	}
	
	public void Fome() {
		System.out.println("O leão está com fome");
		
	}
	
	public void Sono() {
		System.out.println("O leão está descansado");
		
	}
	
	public void Humor() {
		System.out.println("O leão está irritado");
		
	}
}
